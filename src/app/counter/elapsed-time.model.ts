export class ElapsedTime {

    years: number;
    weeks: number;
    days: number;
    hours: number;
    minutes: number;
    seconds: number;

    constructor(years: number, weeks: number, days: number, hours: number, minutes: number, seconds: number) {
        this.years = years;
        this.weeks = weeks;
        this.days = days;
        this.hours = hours;
        this.minutes = minutes;
        this.seconds = seconds;
    }
}
